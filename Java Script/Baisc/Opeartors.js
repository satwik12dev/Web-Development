// arithmetic operators
let a = 10;
let b = 5;
console.log(a + b); // Addition
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a++);
console.log(a);
console.log(++a);
console.log(a--);
console.log(a);
console.log(--a);

//  comparison operators
let x = 10;
let y = "10";
console.log(x > y);
console.log(x < y);
console.log(x >= y);
console.log(x <= y);
console.log(x == y);
console.log(x === y);
console.log(x != y);

// logical operators
let isLoggedIn = true;
let hasSubscription = false;
console.log(isLoggedIn && hasSubscription);
console.log(isLoggedIn || hasSubscription);
console.log(!isLoggedIn);

